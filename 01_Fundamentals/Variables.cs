using System;

class Programa
{
    static void Main()
    {
        // Tipos primitivos
        int idade = 25;                // número inteiro
        double altura = 1.80;          // número decimal (ponto flutuante)
        string nome = "Gustavo";       // texto
        bool estudante = true;         // valor lógico (verdadeiro/falso)

        // Exibe os valores no console
        Console.WriteLine("Nome: " + nome);
        Console.WriteLine("Idade: " + idade);
        Console.WriteLine("Altura: " + altura);
        Console.WriteLine("É estudante? " + estudante);
    }
}
